const parseDate = d3.timeParse("%Y-%m-%d");
for (const stock in stockData) {
  stockData[stock].forEach(d => {
    d.date = parseDate(d.date);
    d.value = +d.value;
  });
}

const margin = { top: 20, right: 30, bottom: 50, left: 50 };
const width = 800 - margin.left - margin.right;
const height = 400 - margin.top - margin.bottom;

const x = d3.scaleTime()
  .domain([
    d3.min(Object.values(stockData).flat(), d => d.date),
    d3.max(Object.values(stockData).flat(), d => d.date)
  ])
  .range([0, width]);

const y = d3.scaleLinear()
  .domain([
    d3.min(Object.values(stockData).flat(), d => d.value),
    d3.max(Object.values(stockData).flat(), d => d.value)
  ])
  .range([height, 0]);

const svg = d3.select("#plot")
  .append("svg")
  .attr("width", width + margin.left + margin.right)
  .attr("height", height + margin.top + margin.bottom)
  .append("g")
  .attr("transform", `translate(${margin.left},${margin.top})`);

svg.append("g")
  .attr("transform", `translate(0,${height})`)
  .call(d3.axisBottom(x).ticks(20));

svg.append("g")
  .call(d3.axisLeft(y));

const line = d3.line()
  .x(d => x(d.date))
  .y(d => y(d.value));

const colors = d3.schemePaired; 
let colorIndex = 0;

const lineMap = {};

for (const stock in stockData) {
  const color = colors[colorIndex % colors.length];

  const path = svg.append("path")
    .datum(stockData[stock])
    .attr("fill", "none")
    .attr("stroke", color) // Assign color
    .attr("stroke-width", 2)
    .attr("class", "stock-line")
    .attr("data-stock", stock) 
    .attr("d", line)
    .on("mouseover", function () {
      highlightStock(stock);
    })
    .on("mouseout", resetHighlight);

  lineMap[stock] = path; 

  d3.select("#buttons")
    .append("button")
    .text(stock)
    .attr("class", "stock-button")
    .style("background-color", color) // Match button color with line color
    .on("mouseover", function () {
      highlightStock(stock);
    })
    .on("mouseout", resetHighlight);

  colorIndex++;
}

// Hover handlers
function highlightStock(stock) {
  d3.selectAll(".stock-line").style("opacity", 0.2);
  lineMap[stock].style("opacity", 1).style("stroke-width", 4);

  // Show stock name
  svg.append("text")
    .attr("id", "tooltip")
    .attr("x", width - 100)
    .attr("y", 20)
    .attr("text-anchor", "end")
    .style("font-size", "14px")
    .style("font-weight", "bold")
    .text(stock);
}

function resetHighlight() {
  d3.selectAll(".stock-line").style("opacity", 1).style("stroke-width", 2);

  svg.select("#tooltip").remove();
}

